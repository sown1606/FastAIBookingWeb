export type TranslationKey = keyof typeof translations.vi;

interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, values?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

const resolveInitialLocale = (): Locale => {
  if (typeof window === "undefined") {
    return DEFAULT_LOCALE;
  }
  const stored = window.localStorage.getItem(STORAGE_KEY);
  return stored === "en" || stored === "vi" ? stored : DEFAULT_LOCALE;
};

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [locale, setLocaleState] = useState<Locale>(resolveInitialLocale);

  useEffect(() => {
    document.documentElement.lang = locale === "vi" ? "vi-VN" : "en-US";
    window.localStorage.setItem(STORAGE_KEY, locale);
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: TranslationKey, values?: Record<string, string | number>) => {
      const template: string = translations[locale][key] ?? translations.vi[key] ?? key;
      if (!values) {
        return template;
      }
      return Object.entries(values).reduce(
        (result, [name, replacement]) => result.replace(`{${name}}`, String(replacement)),
        template
      );
    };

    return {
      locale,
      setLocale: setLocaleState,
      t
    };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return value;
};

export const statusLabelKey = (status: string): TranslationKey | null => {
  const key = `status.${status}` as TranslationKey;
  return key in translations.vi ? key : null;
};
